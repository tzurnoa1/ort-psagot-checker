def normalize_hebrew(text):
    if not text: return ""
    t = str(text).strip()
    t = re.sub(r'[.!?,:;\"״”’\'\-]', '', t)
    t = " ".join(t.split())
    # החלפות מגדר ונוסח לזיהוי חכם
    replacements = {
        "עלייך": "עליך", "הינך": "הנך", "הנה": "הנך", "ביכולתך": "ביכולת",
        "מידיעותייך": "מידיעות", "מידיעותיך": "מידיעות", "מרעיונותייך": "מרעיונות",
        "מרעיונותיך": "מרעיונות", "הישגייך": "הישגים", "הישגיך": "הישגים", 
        "תפקודך": "תפקוד", "היעדרויותיך": "היעדרויות", "ציונך": "ציון", 
        "למידתך": "למידה", "במאמצייך": "במאמץ", "במאמציך": "במאמץ",
        "נוכחת": "נוכח", "משתתפת": "משתתף", "שקדת": "שקד", "ביצעת": "ביצע",
        "היה עליך": "היה עליך", "היה עלייך": "היה עליך", "ידיעותייך": "ידיעות"
    }
    for old, new in replacements.items():
        t = t.replace(old, new)
    return t

