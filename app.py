def normalize_hebrew(text):
    if not text: return ""
    t = str(text).strip()
    for char in ['"', '״', '”', '“', '’', '‘', '´']:
        t = t.replace(char, "'")
    for char in ['.', ',', '!', '?', ':', ';', '-']:
        t = t.replace(char, "")
    t = " ".join(t.split())
    t = t.replace("ייך", "ך").replace("יך", "ך")
    return t

