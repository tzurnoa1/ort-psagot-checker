def normalize_hebrew(text):
    if not text: return ""
    t = str(text).strip()
    
    # ניקוי רווחים וסימני פיסוק
    t = " ".join(t.split())
    for char in ['.', ',', '!', '?', ':', ';', '-']:
        t = t.replace(char, "")
        
    # האחדת גרשיים (מטפל בחנ"ג)
    for char in ['"', '״', '”', '“', '’', '‘', '´']:
        t = t.replace(char, "'")
    
    # --- מנגנון התעלמות מהבדלי מגדר (זכר/נקבה) ---
    # הופך סיומות "ייך" ו-"יך" ל-"ך"
    t = t.replace("ייך", "ך").replace("יך", "ך")
    # מסיר י' בסוף מילה (הופך "הקפידי" ל-"הקפיד", "עשי" ל-"עש")
    # זה עוזר להשוות בין "הקפד" ל"הקפידי"
    words = t.split()
    normalized_words = []
    for w in words:
        if len(w) > 3 and w.endswith('י'):
            normalized_words.append(w[:-1])
        else:
            normalized_words.append(w)
    t = " ".join(normalized_words)
    
    # מילים נפוצות שמשתנות
    t = t.replace("הינך", "הנך")
    t = t.replace("עלייך", "עלך").replace("עליך", "עלך")
    t = t.replace("ביכולתך", "ביכולת")
    t = t.replace("מרעיונותייך", "מרעיונות")
    
    return t
