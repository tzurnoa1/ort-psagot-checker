# --- 2. חילוץ תוספות מה-PDF (אופציונלי) ---
st.sidebar.header("תוספת בנק מה-PDF")
pdf_file = st.sidebar.file_uploader("העלי PDF אם תרצי להוסיף הערות לבנק הקיים", type=['pdf'])

# יצירת עותק של הבנק כדי לא לדרוס את המקור
active_bank = {k: v.copy() for k, v in GRADE_BANK.items()}

if pdf_file:
    with pdfplumber.open(pdf_file) as pdf:
        for page in pdf.pages:
            tables = page.extract_tables()
            for table in tables:
                for row in table:
                    row = [c for c in row if c]
                    if len(row) < 2: continue
                    g_val, n_val = None, None
                    for cell in row:
                        digits = re.findall(r'\d+', str(cell))
                        if digits and not n_val: g_val = int(digits[0])
                        elif len(str(cell)) > 10: n_val = str(cell).strip()
                    if g_val and n_val:
                        if g_val not in active_bank: active_bank[g_val] = []
                        if n_val not in active_bank[g_val]: active_bank[g_val].append(n_val)

# --- 3. סריקת התעודות ---
uploaded_file = st.file_uploader("העלי קובץ תעודות (Word)", type=['docx'])

if uploaded_file:
    doc = Document(uploaded_file)
    all_results = []
    current_student = "לא זוהה שם"
    
    for child in doc._element.body.iter():
        if child.tag.endswith('p'):
            para_text = "".join(t.text for t in child.iter('{http://schemas.openxmlformats.org/wordprocessingml/2006/main}t') if t.text)
            if any(k in para_text for k in ["שם התלמיד", "שם התלמידה", "שם:", "לכבוד"]):
                name = re.sub(r'(שם התלמיד/ה:|שם התלמיד:|שם התלמידה:|שם:|לכבוד|תלמיד:|תלמידה:)', '', para_text)
                name = re.sub(r'[^א-ת\s]', '', name).strip()
                if len(name.split()) >= 2: current_student = name

        elif child.tag.endswith('tbl'):
            from docx.table import Table
            table = Table(child, doc)
            if len(table.rows) < 2: continue
            headers = [c.text.strip() for c in table.rows[0].cells]
            col_grade = next((idx for idx, h in enumerate(headers) if "ציון" in h), -1)
            col_note = next((idx for idx, h in enumerate(headers) if "הערכה" in h or "מילולית" in h), -1)
            col_sub = next((idx for idx, h in enumerate(headers) if "מקצוע" in h), 0)

            if col_grade != -1 and col_note != -1:
                for row in table.rows[1:]:
                    cells = [c.text.strip() for c in row.cells]
                    if len(cells) > max(col_grade, col_note):
                        sub, grade_str, note = cells[col_sub], cells[col_grade], cells[col_note]
                        if not (grade_str or note): continue
                        
                        grade_num = int(re.findall(r'\d+', str(grade_str))[0]) if re.findall(r'\d+', str(grade_str)) else 0
                        error = None
                        found_count = 0
                        
                        if grade_num in active_bank:
                            note_norm = normalize_hebrew(note)
                            valid_options = [normalize_hebrew(opt) for opt in active_bank[grade_num]]
                            
                            remaining = note_norm
                            # לוגיקה שמזהה גם אם המורה השתמש ב-2 הערות מהבנק
                            for opt in sorted(valid_options, key=len, reverse=True):
                                if opt in remaining:
                                    remaining = remaining.replace(opt, "").strip()
                                    found_count += 1
                            
                            remaining_clean = re.sub(r'[.\s,-]+', '', remaining)
                            if found_count == 0: error = "❌ הערה לא נמצאה בבנק"
                            elif len(remaining_clean) > 3: error = f"⚠️ שילוב טקסט חופשי: {remaining}"
                        
                        all_results.append({
                            "תלמיד/ה": current_student, "מקצוע": sub, "ציון": grade_str,
                            "סטטוס": "✅ תקין" if not error else "❌ בדיקה",
                            "פירוט": error if error else f"תקין (זוהו {found_count} משפטים)"
                        })

    if all_results: st.table(pd.DataFrame(all_results))
