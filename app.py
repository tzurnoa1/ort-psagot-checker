# --- 2. עיבוד הנתונים ---
active_bank = {k: [normalize_hebrew(n) for n in v] for k, v in GRADE_BANK.items()}

# תמיכה ב-PDF
pdf_file = st.sidebar.file_uploader("העלי PDF של הבנק (אופציונלי)", type=['pdf'])
if pdf_file:
    with pdfplumber.open(pdf_file) as pdf:
        for page in pdf.pages:
            tables = page.extract_tables()
            for table in tables:
                for row in table:
                    clean_row = [str(c).strip() for c in row if c]
                    if len(clean_row) < 2: continue
                    g_val = None
                    for cell in clean_row:
                        digits = re.findall(r'\d+', cell)
                        if digits: g_val = int(digits[0]); break
                    if g_val:
                        texts = [c for c in clean_row if not re.fullmatch(r'\d+', c)]
                        if texts:
                            n_val = normalize_hebrew(max(texts, key=len))
                            if g_val not in active_bank: active_bank[g_val] = []
                            active_bank[g_val].append(n_val)

# --- 3. בדיקת התעודה ---
uploaded_file = st.file_uploader("העלי קובץ תעודות (Word)", type=['docx'])

if uploaded_file:
    doc = Document(uploaded_file)
    results = []
    current_student = "לא זוהה"

    for table in doc.tables:
        if len(table.rows) < 2: continue
        headers = [c.text.strip() for c in table.rows[0].cells]
        col_grade = next((i for i, h in enumerate(headers) if "ציון" in h), -1)
        col_sub = next((i for i, h in enumerate(headers) if "מקצוע" in h), 0)
        
        if col_grade == -1: continue

        for row in table.rows[1:]:
            cells = [c.text.strip() for c in row.cells]
            if len(cells) <= col_grade: continue
            
            grade_match = re.findall(r'\d+', cells[col_grade])
            if not grade_match: continue
            g_num = int(grade_match[0])
            
            note_candidates = [c for i, c in enumerate(cells) if i != col_grade and i != col_sub and len(c) > 5]
            full_note = max(note_candidates, key=len) if note_candidates else ""
            if not full_note: continue
            
            # פירוק למשפטים ובדיקה מול הבנק (Fuzzy Matching)
            sentences = re.split(r'[.!?\n]+', full_note)
            sentences = [s.strip() for s in sentences if len(s.strip()) > 5]
            
            invalid_parts = []
            if g_num in active_bank:
                for s in sentences:
                    norm_s = normalize_hebrew(s)
                    if not any(difflib.SequenceMatcher(None, norm_s, opt).ratio() > 0.82 for opt in active_bank[g_num]):
                        invalid_parts.append(s)
            
            status = "✅ תקין" if not invalid_parts else "❌ שגיאה"
            detail = "הכל תואם" if not invalid_parts else f"לא מזוהה: {', '.join(invalid_parts)}"

            results.append({
                "מקצוע": cells[col_sub],
                "ציון": g_num,
                "הערכה": full_note,
                "סטטוס": status,
                "פירוט": detail
            })

    if results:
        st.table(pd.DataFrame(results))
