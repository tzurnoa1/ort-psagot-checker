def normalize_hebrew(text):
    if not text: return ""
    t = str(text).strip()
    t = re.sub(r'\s+', ' ', t)
    t = t.replace("ייך", "ך").replace("יך", "ך").replace("הינך", "הנך")
    t = t.replace("עליך", "עלך").replace("עלייך", "עלך")
    return t

