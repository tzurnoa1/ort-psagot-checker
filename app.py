def normalize_hebrew(text):
    if not text: return ""
    t = str(text).strip()
    t = t.replace("ייך", "ך").replace("יך", "ך").replace("הינך", "הנך")
    t = t.replace("עליך", "עלך").replace("עלייך", "עלך")
    return t

def get_text_from_xml(element):
    """שואב טקסט מכל מקום ב-XML, כולל תיבות טקסט"""
    text = ""
    for t in element.iter('{http://schemas.openxmlformats.org/wordprocessingml/2006/main}t'):
        text += t.text
    return text

uploaded_file = st.file_uploader("העלי קובץ תעודות (Word)", type=['docx'])

if uploaded_file:
    doc = Document(uploaded_file)
    all_results = []
    
    # 1. חילוץ כל הבלוקים במסמך (כולל אלו שבתוך תיבות טקסט)
    body = doc._element.body
    current_student = "לא זוהה שם"
    
    # נעבור על כל האלמנטים במסמך לפי הסדר שלהם
    for child in body.iter():
        # אם מצאנו פסקה
        if child.tag.endswith('p'):
            para_text = get_text_from_xml(child)
            
            # אם הפסקה מכילה את העוגן שנתת לי
            if "להלן חוות דעתם" in para_text:
                # השם נמצא כנראה בטקסט שנסרק ממש לפני רגע
                pass 
            
            # חיפוש שם לפי תבנית
            if any(k in para_text for k in ["שם התלמיד", "שם התלמידה", "שם:", "לכבוד"]):
                name = re.sub(r'(שם התלמיד/ה:|שם התלמיד:|שם התלמידה:|שם:|לכבוד|תלמיד:|תלמידה:|מס\' זהות:.*|ת\.ז:.*|כיתה:.*)', '', para_text)
                name = re.sub(r'[^א-ת\s]', '', name).strip()
                if len(name.split()) >= 2:
                    current_student = name

        # אם הגענו לטבלה
        elif child.tag.endswith('tbl'):
            # הפיכת אלמנט ה-XML לאובייקט טבלה של docx
            from docx.table import Table
            table = Table(child, doc)
            
            if len(table.rows) < 2: continue
            headers = [c.text.strip() for c in table.rows[0].cells]
            
            col_grade = next((idx for idx, h in enumerate(headers) if "ציון" in h), -1)
            col_note = next((idx for idx, h in enumerate(headers) if "הערכה" in h or "מילולית" in h), -1)
            col_sub = next((idx for idx, h in enumerate(headers) if "מקצוע" in h), 0)
            
            if col_grade != -1 and col_note != -1:
                for row in table.rows[1:]:
                    cells = [c.text.strip() for c in row.cells]
                    if len(cells) > max(col_grade, col_note):
                        sub, grade, note = cells[col_sub], cells[col_grade], cells[col_note]
                        if not (grade or note): continue
                        
                        # בדיקת הציון
                        nums = re.findall(r'\d+', str(grade))
                        grade_num = int(nums[0]) if nums else 0
                        error = None
                        if grade_num in GRADE_BANK:
                            note_norm = normalize_hebrew(note)
                            if not any(normalize_hebrew(a) in note_norm for a in GRADE_BANK[grade_num]):
                                error = "📝 הערה חופשית"
                        
                        all_results.append({
                            "שם התלמיד/ה": current_student,
                            "מקצוע": sub,
                            "ציון": grade,
                            "הערכה מילולית": note,
                            "סטטוס": "✅ תקין" if not error else "❌ בדיקה",
                            "פירוט": error if error else "תקין"
                        })

    if all_results:
        df = pd.DataFrame(all_results)
        st.table(df)
    else:
        st.error("לא נמצאו טבלאות. ודאי שיש עמודה בשם 'ציון'.")
