# יצירת עותק של הבנק כדי לא לדרוס את המקור
active_bank = {k: v.copy() for k, v in GRADE_BANK.items()}

if pdf_file:
    with pdfplumber.open(pdf_file) as pdf:
        for page in pdf.pages:
            tables = page.extract_tables()
            for table in tables:
                for row in table:
                    # מנקה תאים ריקים
                    row = [str(c).strip() for c in row if c]
                    if len(row) < 2: continue
                    
                    g_val, n_val = None, None
                    potential_notes = []

                    for cell in row:
                        # מחפש את הציון (מספר)
                        digits = re.findall(r'\d+', cell)
                        if digits and not g_val:
                            g_val = int(digits[0])
                        # מחפש טקסט ארוך (הערה) - לפחות 15 תווים
                        elif len(cell) > 15:
                            potential_notes.append(cell)
                    
                    # אם מצאנו כמה טקסטים, ניקח את הארוך ביותר (הוא בטוח ההערה ולא המקצוע)
                    if potential_notes:
                        n_val = max(potential_notes, key=len)

                    if g_val and n_val:
                        if g_val not in active_bank: active_bank[g_val] = []
                        if n_val not in active_bank[g_val]: 
                            active_bank[g_val].append(n_val)

# --- 3. סריקת התעודות ---
uploaded_file = st.file_uploader("העלי קובץ תעודות (Word)", type=['docx'])

if uploaded_file:
    doc = Document(uploaded_file)
    all_results = []
    current_student = "לא זוהה שם"
    
    for child in doc._element.body.iter():
        if child.tag.endswith('p'):
            para_text = "".join(t.text for t in child.iter('{http://schemas.openxmlformats.org/wordprocessingml/2006/main}t') if t.text)
            if any(k in para_text for k in ["שם התלמיד", "שם התלמידה", "שם:", "לכבוד"]):
                name = re.sub(r'(שם התלמיד/ה:|שם התלמיד:|שם התלמידה:|שם:|לכבוד|תלמיד:|תלמידה:)', '', para_text)
                name = re.sub(r'[^א-ת\s]', '', name).strip()
                if len(name.split()) >= 2: current_student = name

        elif child.tag.endswith('tbl'):
            from docx.table import Table
            table = Table(child, doc)
            if len(table.rows) < 2: continue
            
            # זיהוי אינדקסים של עמודות לפי כותרות
            headers = [c.text.strip() for c in table.rows[0].cells]
            col_grade = next((idx for idx, h in enumerate(headers) if "ציון" in h), -1)
            # אנחנו מחפשים עמודה שכתוב בה "הערכה" או "מילולית"
            col_note_index = next((idx for idx, h in enumerate(headers) if "הערכה" in h or "מילולית" in h), -1)
            col_sub = next((idx for idx, h in enumerate(headers) if "מקצוע" in h), 0)

            if col_grade != -1:
                for row in table.rows[1:]:
                    cells = [c.text.strip() for c in row.cells]
                    if len(cells) > col_grade:
                        # לוגיקת שליפה חכמה:
                        grade_str = cells[col_grade]
                        
                        # אם מצאנו עמודת הערכה לפי כותרת - ניקח אותה. 
                        # אם לא, נחפש בתא אחר שהוא לא המקצוע ולא הציון והוא הכי ארוך.
                        potential_note = ""
                        if col_note_index != -1 and col_note_index < len(cells):
                            potential_note = cells[col_note_index]
                        else:
                            # לוקח את הטקסט הכי ארוך בשורה שאינו הציון
                            text_cells = [c for i, c in enumerate(cells) if i != col_grade and len(c) > 2]
                            if text_cells:
                                potential_note = max(text_cells, key=len)
                        
                        sub = cells[col_sub] if col_sub < len(cells) else "לא ידוע"
                        note = potential_note
                        
                        if not (grade_str or note): continue
                        
                        # המשך הבדיקה מול הבנק...
                        grade_num_match = re.findall(r'\d+', str(grade_str))
                        grade_num = int(grade_num_match[0]) if grade_num_match else 0
                        error = None
                        found_count = 0
                        
                        if grade_num in active_bank:
                            note_norm = normalize_hebrew(note)
                            valid_options = [normalize_hebrew(opt) for opt in active_bank[grade_num]]
                            
                            remaining = note_norm
                            for opt in sorted(valid_options, key=len, reverse=True):
                                if opt in remaining:
                                    remaining = remaining.replace(opt, "").strip()
                                    found_count += 1
                            
                            remaining_clean = re.sub(r'[.\s,-]+', '', remaining)
                            if found_count == 0: 
                                error = "❌ הערה לא נמצאה בבנק"
                            elif len(remaining_clean) > 3: 
                                error = f"⚠️ שילוב טקסט חופשי: {remaining}"
                        
                        all_results.append({
                            "תלמיד/ה": current_student,
                            "מקצוע": sub,
                            "ציון": grade_str,
                            "הערכה בתעודה": note, # כאן תראי מה הוא שלף
                            "סטטוס": "✅ תקין" if not error else "❌ בדיקה",
                            "פירוט": error if error else f"תקין (זוהו {found_count} משפטים)"
                        })

    if all_results: st.table(pd.DataFrame(all_results))
