def get_bank_for_grade(grade):
    # מוצא את המפתח הכי קרוב מלמטה
    sorted_keys = sorted(GRADE_BANK.keys(), reverse=True)
    for key in sorted_keys:
        if grade >= key: return GRADE_BANK[key]
    return []

# --- לוגיקת האפליקציה ---
docx_file = st.file_uploader("העלי קובץ תעודה (Word)", type=['docx'])

# רשימה שטוחה לזיהוי "האם בכלל בבנק"
all_valid_norm = [normalize_hebrew(s) for sublist in GRADE_BANK.values() for s in sublist]

if docx_file:
    doc = Document(docx_file)
    results = []
    current_student = "לא זוהה"

    for element in doc.element.body.iter():
        if element.tag.endswith('p'):
            text = "".join(t.text for t in element.iter('{http://schemas.openxmlformats.org/wordprocessingml/2006/main}t') if t.text)
            if any(k in text for k in ["שם התלמיד", "שם התלמידה"]):
                current_student = text.split(":", 1)[1].strip() if ":" in text else text
        
        elif element.tag.endswith('tbl'):
            from docx.table import Table
            table = Table(element, doc)
            if len(table.rows) < 2: continue
            
            headers = [c.text.strip() for c in table.rows[0].cells]
            col_grade = next((i for i, h in enumerate(headers) if "ציון" in h), -1)
            col_sub = next((i for i, h in enumerate(headers) if "מקצוע" in h), 0)
            
            if col_grade != -1:
                for row in table.rows[1:]:
                    cells = [c.text.strip() for c in row.cells]
                    if len(cells) <= col_grade: continue
                    g_match = re.findall(r'\d+', cells[col_grade])
                    if not g_match: continue
                    grade_val = int(g_match[0])
                    
                    notes_cands = [c for i, c in enumerate(cells) if i != col_grade and i != col_sub and len(c) > 5]
                    note_text = max(notes_cands, key=len) if notes_cands else ""
                    
                    if note_text:
                        # פירוק למשפטים
                        sentences = re.split(r'[.!?\n]+', note_text)
                        sentences = [s.strip() for s in sentences if len(s.strip()) > 5]
                        
                        not_in_bank = []
                        grade_matches = []
                        
                        # בנק ספציפי לציון
                        grade_bank = [normalize_hebrew(s) for s in get_bank_for_grade(grade_val)]
                        
                        for s in sentences:
                            norm_s = normalize_hebrew(s)
                            if not any(norm_s in b or b in norm_s for b in all_valid_norm):
                                not_in_bank.append(s)
                            
                            is_match = any(norm_s in b or b in norm_s for b in grade_bank)
                            grade_matches.append("✅" if is_match else "❌")

                        results.append({
                            "תלמיד/ה": current_student,
                            "מקצוע": cells[col_sub],
                            "ציון": grade_val,
                            "הערה": note_text,
                            "הערות שלא נמצאות בבנק": ", ".join(not_in_bank) if not_in_bank else "",
                            "תואם לציון?": " | ".join(grade_matches)
                        })

    if results:
        st.table(pd.DataFrame(results))
